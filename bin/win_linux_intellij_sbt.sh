# 方法1 SBT方式 
# 1 下载安装JDK
# http://www.oracle.com/technetwork/java/javase/downloads/index.html

# 2 下载安装intellij 
# http://www.jetbrains.com/idea/

# 3 安装Scala插件
# 依次选择“Configure”–> “Plugins”–> “Browse repositories”，输入scala，然后安装即可

# 4 下载SBT项目模板，引入Spark依赖包

# 5 sbt assembly

# 6 sbt gen-idea

# 7 intellij引入项目,运行示例



